	e1:SetType(EFFECT_TYPE_FIELD+EFFECT_TYPE_CONTINUOUS)
	e1:SetCode(EVENT_PHASE_START+PHASE_DRAW)
	e1:SetRange(LOCATION_EXTRA)
	e1:SetOperation(c98942059.op)
	e1:SetCountLimit(1)
	c:RegisterEffect(e1)	
end
