--千年城
function c22023000.initial_effect(c)
	--Activate
	local e1=Effect.CreateEffect(c)
	e1:SetType(EFFECT_TYPE_ACTIVATE)
	e1:SetCode(EVENT_FREE_CHAIN)
	c:RegisterEffect(e1)
	--fusion
	local e2=Effect.CreateEffect(c)
	e2:SetType(EFFECT_TYPE_FIELD)
	e2:SetProperty(EFFECT_FLAG_SET_AVAILABLE+EFFECT_FLAG_IGNORE_RANGE+EFFECT_FLAG_IGNORE_IMMUNE)
	e2:SetRange(LOCATION_FZONE)
	e2:SetCode(EFFECT_TO_GRAVE_REDIRECT)
	e2:SetTargetRange(0xff,0)
	e2:SetValue(LOCATION_REMOVED)
	e2:SetCondition(c22023000.fucon1)
	c:RegisterEffect(e2)
	local e3=e2:Clone()
	e3:SetCondition(c22023000.fucon2)
	e3:SetTargetRange(0,0xff)
	c:RegisterEffect(e3)
	--Synchro
	local e4=Effect.CreateEffect(c)
	e4:SetType(EFFECT_TYPE_FIELD)
	e4:SetCode(EFFECT_CANNOT_TO_HAND)
	e4:SetRange(LOCATION_FZONE)
	e4:SetProperty(EFFECT_FLAG_PLAYER_TARGET)
	e4:SetTargetRange(1,0)
	e4:SetTarget(aux.TargetBoolFunction(Card.IsLocation,LOCATION_DECK))
	e4:SetCondition(c22023000.sycon1)
	c:RegisterEffect(e4)
	local e5=e4:Clone()
	e5:SetCondition(c22023000.sycon2)
	e5:SetTargetRange(0,1)
	c:RegisterEffect(e5)
	--cannot set
	local e6=Effect.CreateEffect(c)
	e6:SetType(EFFECT_TYPE_FIELD)
	e6:SetCode(EFFECT_CANNOT_MSET)
	e6:SetProperty(EFFECT_FLAG_PLAYER_TARGET)
	e6:SetRange(LOCATION_FZONE)
	e6:SetTargetRange(0,1)
	e6:SetTarget(aux.TRUE)
	e6:SetCondition(c22023000.xyzcon1)
	c:RegisterEffect(e6)
	local e7=e6:Clone()
	e7:SetCode(EFFECT_CANNOT_SSET)
	c:RegisterEffect(e7)
	local e8=e6:Clone()
	e8:SetCode(EFFECT_CANNOT_TURN_SET)
	c:RegisterEffect(e8)
	local e9=e6:Clone()
	e9:SetCode(EFFECT_LIMIT_SPECIAL_SUMMON_POSITION)
	e9:SetTarget(c22023000.sumlimit)
	c:RegisterEffect(e9)
	--cannot set
	local e10=Effect.CreateEffect(c)
	e10:SetType(EFFECT_TYPE_FIELD)
	e10:SetCode(EFFECT_CANNOT_MSET)
	e10:SetProperty(EFFECT_FLAG_PLAYER_TARGET)
	e10:SetRange(LOCATION_FZONE)
	e10:SetTargetRange(1,0)
	e10:SetTarget(aux.TRUE)
	e10:SetCondition(c22023000.xyzcon2)
	c:RegisterEffect(e10)
	local e11=e10:Clone()
	e11:SetCode(EFFECT_CANNOT_SSET)
	c:RegisterEffect(e11)
	local e12=e10:Clone()
	e12:SetCode(EFFECT_CANNOT_TURN_SET)
	c:RegisterEffect(e12)
	local e13=e10:Clone()
	e13:SetCode(EFFECT_LIMIT_SPECIAL_SUMMON_POSITION)
	e13:SetTarget(c22023000.sumlimit)
	c:RegisterEffect(e13)
	--cannot attack
	local e14=Effect.CreateEffect(c)
	e14:SetType(EFFECT_TYPE_FIELD)
	e14:SetCode(EFFECT_CANNOT_ATTACK_ANNOUNCE)
	e14:SetRange(LOCATION_FZONE)
	e14:SetTargetRange(LOCATION_MZONE,0)
	e14:SetCondition(c22023000.lkcon1)
	c:RegisterEffect(e14)
	--cannot attack
	local e14=Effect.CreateEffect(c)
	e14:SetType(EFFECT_TYPE_FIELD)
	e14:SetCode(EFFECT_CANNOT_ATTACK_ANNOUNCE)
	e14:SetRange(LOCATION_FZONE)
	e14:SetTargetRange(0,LOCATION_MZONE)
	e14:SetCondition(c22023000.lkcon2)
	c:RegisterEffect(e14)
end
function c22023000.filter(c)
	return c:IsCode(22023010) and c:IsFaceup()
end
function c22023000.ffilter(c)
	return c:IsType(TYPE_FUSION) and c:IsFaceup()
end
function c22023000.fucon1(e)
	return Duel.IsExistingMatchingCard(c22023000.ffilter,tp,LOCATION_MZONE,0,1,nil) and not Duel.IsExistingMatchingCard(c22023000.filter,tp,LOCATION_ONFIELD,0,1,nil)
end
function c22023000.fucon2(e)
	return Duel.IsExistingMatchingCard(c22023000.ffilter,tp,LOCATION_MZONE,1,0,nil) or Duel.IsExistingMatchingCard(c22023000.filter,tp,LOCATION_ONFIELD,0,1,nil)
end
function c22023000.sfilter(c)
	return c:IsType(TYPE_SYNCHRO) and c:IsFaceup()
end
function c22023000.sycon1(e)
	return Duel.IsExistingMatchingCard(c22023000.sfilter,tp,LOCATION_MZONE,0,1,nil) and not Duel.IsExistingMatchingCard(c22023000.filter,tp,LOCATION_ONFIELD,0,1,nil)
end
function c22023000.sycon2(e)
	return Duel.IsExistingMatchingCard(c22023000.sfilter,tp,LOCATION_MZONE,1,0,nil) or Duel.IsExistingMatchingCard(c22023000.filter,tp,LOCATION_ONFIELD,0,1,nil)
end
function c22023000.sumlimit(e,c,sump,sumtype,sumpos,targetp)
	return bit.band(sumpos,POS_FACEDOWN)>0
end
function c22023000.xfilter(c)
	return c:IsType(TYPE_XYZ) and c:IsFaceup()
end
function c22023000.xyzcon1(e)
	return Duel.IsExistingMatchingCard(c22023000.xfilter,tp,LOCATION_MZONE,0,1,nil) and not Duel.IsExistingMatchingCard(c22023000.filter,tp,LOCATION_ONFIELD,0,1,nil)
end
function c22023000.xyzcon2(e)
	return Duel.IsExistingMatchingCard(c22023000.xfilter,tp,LOCATION_MZONE,1,0,nil) or Duel.IsExistingMatchingCard(c22023000.filter,tp,LOCATION_ONFIELD,0,1,nil)
end
function c22023000.lfilter(c)
	return c:IsType(TYPE_LINK) and c:IsFaceup()
end
function c22023000.lkcon1(e)
	return Duel.IsExistingMatchingCard(c22023000.lfilter,tp,LOCATION_MZONE,0,1,nil) and not Duel.IsExistingMatchingCard(c22023000.filter,tp,LOCATION_ONFIELD,0,1,nil)
end
function c22023000.lkcon2(e)
	return Duel.IsExistingMatchingCard(c22023000.lfilter,tp,LOCATION_MZONE,1,0,nil) or Duel.IsExistingMatchingCard(c22023000.filter,tp,LOCATION_ONFIELD,0,1,nil)
end