--真理字界眼龙
function c79200023.initial_effect(c)
	aux.EnablePendulumAttribute(c,false)
	local e0=Effect.CreateEffect(c)
	e0:SetType(EFFECT_TYPE_SINGLE)
	e0:SetCode(EFFECT_SPSUMMON_COST)
	e0:SetOperation(c79200023.spop)
	c:RegisterEffect(e0)
	local e2=Effect.CreateEffect(c)
	e2:SetDescription(aux.Stringid(79200023,0))
	e2:SetType(EFFECT_TYPE_FIELD+EFFECT_TYPE_TRIGGER_O)
	e2:SetCategory(CATEGORY_DAMAGE)
	e2:SetCode(EVENT_PHASE+PHASE_DRAW)
	e2:SetRange(LOCATION_DECK)
	e2:SetCountLimit(1,79200023+EFFECT_COUNT_CODE_DUEL)
	e2:SetCondition(c79200023.excon)
	e2:SetOperation(c79200023.exop)
	c:RegisterEffect(e2)
	local e4=Effect.CreateEffect(c)
	e4:SetType(EFFECT_TYPE_SINGLE)
	e4:SetProperty(EFFECT_FLAG_SINGLE_RANGE)
	e4:SetCode(EFFECT_UPDATE_ATTACK)
	e4:SetRange(LOCATION_MZONE)
	e4:SetValue(c79200023.value)
	c:RegisterEffect(e4)
	local e5=Effect.CreateEffect(c)
	e5:SetType(EFFECT_TYPE_SINGLE)
	e5:SetCode(EFFECT_DIRECT_ATTACK)
	c:RegisterEffect(e5)
end
function c79200023.spop(e,tp,eg,ep,ev,re,r,rp)
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(79200023,8))
	Duel.SelectOption(tp,aux.Stringid(79200023,1))
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(79200023,8))
	Duel.SelectOption(tp,aux.Stringid(79200023,2))
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(79200023,8))
	Duel.SelectOption(tp,aux.Stringid(79200023,3))
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(79200023,8))
	Duel.SelectOption(tp,aux.Stringid(79200023,4))
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(79200023,8))
	Duel.SelectOption(tp,aux.Stringid(79200023,5))
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(79200023,8))
	Duel.SelectOption(tp,aux.Stringid(79200023,6))
	Duel.Hint(HINT_SELECTMSG,tp,aux.Stringid(79200023,8))
	Duel.SelectOption(tp,aux.Stringid(79200023,7))
	Duel.Hint(HINT_MUSIC,0,aux.Stringid(79200023,9))
end
function c79200023.excon()
	return Duel.GetTurnCount()==1
end
function c79200023.exop(e,tp,eg,ep,ev,re,r,rp,chk)
	local c=e:GetHandler()
	aux.EnablePendulumAttribute(c,false)
	local e2=Effect.CreateEffect(c)
	e2:SetType(EFFECT_TYPE_SINGLE)
	e2:SetCode(EFFECT_ADD_TYPE)
	e2:SetValue(TYPE_PENDULUM)
	e2:SetReset(RESET_EVENT+RESETS_STANDARD)
	c:RegisterEffect(e2,true)
	Duel.SendtoExtraP(c,tp,REASON_EFFECT)
	local e1=Effect.CreateEffect(e:GetHandler())
	e1:SetType(EFFECT_TYPE_FIELD)
	e1:SetProperty(EFFECT_FLAG_PLAYER_TARGET)
	e1:SetCode(EFFECT_SKIP_DP)
	e1:SetTargetRange(1,0)
	if Duel.GetTurnPlayer()==tp and Duel.GetCurrentPhase()==PHASE_DRAW then
		e1:SetReset(RESET_PHASE+PHASE_DRAW+RESET_SELF_TURN,2)
	else
		e1:SetReset(RESET_PHASE+PHASE_DRAW+RESET_SELF_TURN)
	end
	Duel.RegisterEffect(e1,tp)
end
function c79200023.value(e,c)
	return Duel.GetMatchingGroupCount(Card.IsFacedown,0,LOCATION_REMOVED,LOCATION_REMOVED,nil)*1000
end
