function c22021210.erecost(e,tp,eg,ep,ev,re,r,rp,chk)
	if chk==0 then return true end
	Duel.Hint(HINT_CARD,0,22020980)
	Duel.PayLPCost(tp,math.floor(Duel.GetLP(tp)/2))
end
function c22021210.tgcon1(e,tp,eg,ep,ev,re,r,rp)
	return eg:IsExists(c22021210.cfilter,1,nil,1-tp) or eg:IsExists(c22021210.cfilter,1,nil,tp) and Duel.IsPlayerAffectedByEffect(tp,22020980)
end